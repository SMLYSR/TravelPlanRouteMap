import Foundation

/// 标准间距系统
enum Spacing {
    static let xs: CGFloat = 4      // 紧密相关元素（图标+文字）
    static let sm: CGFloat = 8      // 同组内元素
    static let md: CGFloat = 16     // 标准间距（最常用）
    static let lg: CGFloat = 24     // 区块间距
    static let xl: CGFloat = 32     // 大区块间距
    static let xxl: CGFloat = 48    // 页面顶部/底部留白
}
